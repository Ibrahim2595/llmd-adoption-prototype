export function NavCards({
  columns = 2,
  children,
}: {
  columns?: number
  children: React.ReactNode
}) {
  return (
    <div
      className="grid gap-4 my-8"
      style={{ gridTemplateColumns: `repeat(${columns}, minmax(0, 1fr))` }}
    >
      {children}
    </div>
  )
}

export function NavCard({
  title,
  description,
  href,
  icon,
}: {
  title: string
  description?: string
  href: string
  icon?: string
}) {
  const isExternal = href.startsWith('http')
  const IconEl = icon ? (ICONS[icon] ?? <PlaceholderIcon />) : <PlaceholderIcon />

  const inner = (
    <>
      <div className="flex items-start justify-between mb-2">
        <span className="text-gray-500 dark:text-gray-400">{IconEl}</span>
        {isExternal && (
          <span className="text-gray-400 dark:text-gray-500">
            <ExternalArrow />
          </span>
        )}
      </div>
      <p className="text-base font-semibold text-gray-900 dark:text-gray-50">{title}</p>
      {description && (
        <p className="text-sm text-gray-500 dark:text-gray-400 mt-1 leading-relaxed">{description}</p>
      )}
    </>
  )

  const className =
    'block bg-white dark:bg-gray-800 border border-gray-200 dark:border-gray-700 rounded-lg p-5 hover:shadow-sm transition-shadow cursor-pointer no-underline'

  if (isExternal) {
    return (
      <a href={href} target="_blank" rel="noopener noreferrer" className={className}>
        {inner}
      </a>
    )
  }

  return (
    <Link href={href} className={className}>
      {inner}
    </Link>
  )
}
